//WORDS IN JAVA

//In Java, when we want to write words we can use single quotes or double quotes, but they are different.
//Double quotes are used when writing strings, a set of characteres.
//While single quotes are used with single characteres.


public class WordsInJava {

    public static void main(String[] args){

        // See, double quotes are used with strings...
        System.out.println("Here we have a string"); // => Here we have a string

        // Have atention that, in Java single quotes are used with single characteres 
        System.out.print('G');
        System.out.print('a');
        System.out.print('b');
        System.out.print('r');
        System.out.print('i');
        System.out.print('e');
        System.out.print('l'); // => Gabriel

    }

}